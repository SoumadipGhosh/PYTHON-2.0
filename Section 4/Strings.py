# indexing ..
name="Harry"
print(name[0]) # counting start from 0..
print(name[1]) 
print(name[-1]) # last character..same as name[4] in this case..
print(name[-2]) # second last character..same as name[3] in this case..
print(name[-3]) # third last character..same as name[2] in this case..
print(name[-4]) # fourth last character..same as name[1] in this case..
print(name[-5]) # fifth last character..same as name[0] in this case..